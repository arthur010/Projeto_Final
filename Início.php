<h2>
<p>A Pizza Hut foi fundada em junho de 1958 por dois estudantes da Universidade Estadual de Wichita, os irmãos Dan e Frank Carney, em Wichita, Kansas, Estados Unidos. Antes de fechar em 2015, a mais antiga Pizza Hut continuamente aberta ficava em Manhattan, Kansas, em um distrito comercial e de tabernas conhecido como Aggieville, perto da Universidade Estadual do Kansas. O primeiro restaurante da Pizza Hut a leste do Mississippi foi aberto em Athens, Ohio, em 1966, por Lawrence Berberick e Gary Meyers.</p>
</h2>   
 
